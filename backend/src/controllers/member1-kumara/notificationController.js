// ✅ NEW: Send manual notification from form
// POST /api/notifications/send-manual
// -------------------------
async function sendManualNotification(req, res) {
  try {
    const body = req.body || {};

    const stationId = String(body.station_id || "UNKNOWN").trim() || "UNKNOWN";
    const channel = String(body.channel || "email").toLowerCase();
    const roles = Array.isArray(body.roles) ? body.roles : [];
    const severityText = String(body.severity || "Warning"); // Advisory/Warning/Critical
    const message = String(body.message || "").trim();

    const rows = Array.isArray(body.rows) ? body.rows : [];
    const events = Array.isArray(body.events) ? body.events : [];
    const file_name = String(body.file_name || "").trim();
    const threshold = body.threshold;

    // decide level: use selected severity if provided, otherwise auto
    let level = levelFromSeverityText(severityText);
    if (!level) level = decideAlertLevel({ rows, events });

    const severity = severityLabel(level);

    if (!roles.length) {
      return res.status(400).json({
        ok: false,
        error: "Please select at least one recipient role (SUPERVISOR/MANAGER/SENIOR_MANAGER).",
      });
    }

    const recipients = await User.find({
      is_active: true,
      role: { $in: roles },
      $or: [{ station_id: stationId }, { station_id: "" }, { station_id: { $exists: false } }],
    }).select("email full_name role").lean();

    const emails = recipients.map(r => r.email).filter(Boolean);

    if (!emails.length) {
      const doc = await Notification.create({
        source: "misbehavior_scan",
        triggered_by: "manual_form",
        station_id: stationId,
        alert_level: level,
        severity,
        channel,
        recipients: [],
        status: "failed",
        error: "No recipients found in DB for selected roles/station.",
        payload: body,
      });

      return res.json({ ok: true, status: doc.status, message: doc.error, doc });
    }

    const dedupe_key = buildDedupeKey({ stationId, level, rows });
    const cooldown = cooldownMinutes(level);

    const recent = await Notification.findOne({
      dedupe_key,
      createdAt: { $gte: new Date(Date.now() - cooldown * 60 * 1000) },
      status: { $in: ["sent", "suppressed"] },
    }).lean();

    if (recent) {
      const doc = await Notification.create({
        source: "misbehavior_scan",
        triggered_by: "manual_form",
        station_id: stationId,
        alert_level: level,
        severity,
        channel,
        recipients: emails,
        dedupe_key,
        status: "suppressed",
        error: `Cooldown active (${cooldown} min).`,
        payload: body,
      });

      return res.json({ ok: true, status: doc.status, message: doc.error, doc });
    }

    // Send
    let mailResult = null;
    if (channel === "email") {
      const subject = `FuelWatch ${severity} Alert • Station ${stationId}`;
      const preview = rows.filter(r => (r.pred === 1 || r.pred === true)).slice(0, 6);

      const html = htmlEmailTemplate({
        title: "FuelWatch Manual Alert",
        stationId,
        severity,
        message,
        summary: `File: ${file_name || "—"} • Threshold: ${threshold ?? "—"}`,
        rowsPreview: preview,
      });

      try {
        mailResult = await sendEmail({ to: emails, subject, html });
      } catch (err) {
        const doc = await Notification.create({
          source: "misbehavior_scan",
          triggered_by: "manual_form",
          station_id: stationId,
          alert_level: level,
          severity,
          channel,
          recipients: emails,
          dedupe_key,
          status: "failed",
          error: String(err?.message || err),
          payload: body,
        });

        return res.json({ ok: true, status: doc.status, message: doc.error, doc });
      }
    }

    const doc = await Notification.create({
      source: "misbehavior_scan",
      triggered_by: "manual_form",
      station_id: stationId,
      alert_level: level,
      severity,
      channel,
      recipients: emails,
      dedupe_key,
      status: "sent",
      sent_at: new Date(),
      payload: body,
      mail_result: mailResult,
    });

    return res.json({ ok: true, status: doc.status, message: "Alert sent successfully", doc });
  } catch (e) {
    return res.status(500).json({ ok: false, error: String(e?.message || e) });
  }
}

