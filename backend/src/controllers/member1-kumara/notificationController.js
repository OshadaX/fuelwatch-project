const { sendEmail } = require("../../utils/mailer");

const {
  severityLabel,
  pickStationId,
  decideAlertLevel,
  cooldownMinutes,
  buildDedupeKey,
} = require("../../utils/alertPolicy");

// -------------------------
// Helpers
// -------------------------
function levelFromSeverityText(sev) {
  const s = String(sev || "").toLowerCase();
  if (s === "critical") return 3;
  if (s === "warning") return 2;
  if (s === "advisory" || s === "info") return 1;
  return 0;
}

function htmlEmailTemplate({ title, stationId, severity, message, summary, rowsPreview }) {
  return `
  <div style="font-family:Arial,sans-serif;line-height:1.5">
    <h2 style="margin:0 0 8px">${title}</h2>
    <p style="margin:0 0 10px">
      <b>Station:</b> ${stationId}<br/>
      <b>Severity:</b> ${severity}<br/>
      ${summary ? `<b>Summary:</b> ${summary}<br/>` : ""}
    </p>
    ${message ? `<p style="padding:10px;border-left:4px solid #d32f2f;background:#fff4f4">${message}</p>` : ""}
    ${rowsPreview?.length ? `
      <h3>Top Anomalies</h3>
      <table border="1" cellpadding="6" cellspacing="0" style="border-collapse:collapse;font-size:13px">
        <thead>
          <tr>
            <th>Day</th><th>Fuel</th><th>Prob</th><th>Reason</th>
          </tr>
        </thead>
        <tbody>
          ${rowsPreview.map(r => `
            <tr>
              <td>${r.day || ""}</td>
              <td>${r.fuelType || r.fuel_type || ""}</td>
              <td>${Number(r.prob ?? r.prob_irregular ?? 0).toFixed(3)}</td>
              <td>${(r.reason || "").toString().slice(0,160)}</td>
            </tr>
          `).join("")}
        </tbody>
      </table>
    ` : ""}
    <p style="margin-top:14px;color:#666;font-size:12px">
      FuelWatch Alert System • This message was generated by the anomaly detection module.
    </p>
  </div>
  `;
}

// -------------------------
// GET recipients for UI
// /api/notifications/recipients?station_id=Premium%20Tank%2003
// -------------------------
async function getRecipients(req, res) {
  try {
    const station_id = String(req.query.station_id || "").trim();

    // fetch active management users
    const q = {
      is_active: true,
      role: { $in: ["SUPERVISOR", "MANAGER", "SENIOR_MANAGER"] },
    };

    // optional station filter: if station_id is stored
    // allow users with empty station_id too (global managers)
    if (station_id) {
      q.$or = [{ station_id }, { station_id: "" }, { station_id: { $exists: false } }];
    }

    const users = await User.find(q).select("full_name email role station_id").lean();

    return res.json({
      ok: true,
      station_id: station_id || null,
      recipients: users,
    });
  } catch (e) {
    return res.status(500).json({ ok: false, error: String(e?.message || e) });
  }
}

