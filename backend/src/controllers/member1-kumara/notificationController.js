// -------------------------
// Existing: notify from scan (button old workflow)
// -------------------------
async function notifyFromScan(req, res) {
  try {
    const payload = req.body || {};
    const rows = Array.isArray(payload.rows) ? payload.rows : [];
    const events = Array.isArray(payload.events) ? payload.events : [];

    const stationId = payload.station_id || pickStationId(payload) || "UNKNOWN";
    const level = decideAlertLevel({ rows, events });
    const severity = severityLabel(level);

    const roles = level >= 3
      ? ["SUPERVISOR", "MANAGER", "SENIOR_MANAGER"]
      : level === 2
      ? ["SUPERVISOR", "MANAGER"]
      : level === 1
      ? ["SUPERVISOR"]
      : [];

    if (!roles.length) {
      const doc = await Notification.create({
        source: "misbehavior_scan",
        triggered_by: "manual",
        station_id: stationId,
        alert_level: level,
        severity,
        channel: "email",
        recipients: [],
        status: "suppressed",
        error: "No anomalies flagged",
        payload,
      });
      return res.json({ ok: true, status: doc.status, message: "No anomalies to notify.", doc });
    }

    const recipients = await User.find({
      is_active: true,
      role: { $in: roles },
      $or: [{ station_id: stationId }, { station_id: "" }, { station_id: { $exists: false } }],
    }).select("email full_name role").lean();

    const emails = recipients.map(r => r.email).filter(Boolean);

    if (!emails.length) {
      const doc = await Notification.create({
        source: "misbehavior_scan",
        triggered_by: "manual",
        station_id: stationId,
        alert_level: level,
        severity,
        channel: "email",
        recipients: [],
        status: "failed",
        error: "No recipients found. Add users with roles SUPERVISOR/MANAGER/SENIOR_MANAGER.",
        payload,
      });
      return res.json({ ok: true, status: doc.status, message: doc.error, doc });
    }

    const dedupe_key = buildDedupeKey({ stationId, level, rows });
    const cooldown = cooldownMinutes(level);

    const recent = await Notification.findOne({
      dedupe_key,
      createdAt: { $gte: new Date(Date.now() - cooldown * 60 * 1000) },
      status: { $in: ["sent", "suppressed"] },
    }).lean();

    if (recent) {
      const doc = await Notification.create({
        source: "misbehavior_scan",
        triggered_by: "manual",
        station_id: stationId,
        alert_level: level,
        severity,
        channel: "email",
        recipients: emails,
        dedupe_key,
        status: "suppressed",
        error: `Cooldown active (${cooldown} min).`,
        payload,
      });
      return res.json({ ok: true, status: doc.status, message: doc.error, doc });
    }

    const subject = `FuelWatch ${severity} Alert • Station ${stationId}`;
    const preview = rows.filter(r => (r.pred === 1 || r.pred === true)).slice(0, 6);

    const html = htmlEmailTemplate({
      title: "FuelWatch Anomaly Alert",
      stationId,
      severity,
      message: payload.message || "",
      summary: `Flagged anomalies detected. Level=${level}`,
      rowsPreview: preview,
    });

    let mailResult = null;
    try {
      mailResult = await sendEmail({ to: emails, subject, html });
    } catch (err) {
      const doc = await Notification.create({
        source: "misbehavior_scan",
        triggered_by: "manual",
        station_id: stationId,
        alert_level: level,
        severity,
        channel: "email",
        recipients: emails,
        dedupe_key,
        status: "failed",
        error: String(err?.message || err),
        payload,
      });
      return res.json({ ok: true, status: doc.status, message: doc.error, doc });
    }

    const doc = await Notification.create({
      source: "misbehavior_scan",
      triggered_by: "manual",
      station_id: stationId,
      alert_level: level,
      severity,
      channel: "email",
      recipients: emails,
      dedupe_key,
      status: "sent",
      sent_at: new Date(),
      payload,
      mail_result: mailResult,
    });

    return res.json({ ok: true, status: doc.status, message: "Notification sent", doc });
  } catch (e) {
    return res.status(500).json({ ok: false, error: String(e?.message || e) });
  }
}

